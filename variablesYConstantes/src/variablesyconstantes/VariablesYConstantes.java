
package variablesyconstantes;

public class VariablesYConstantes {

    
    public static void main(String[] args) {
        final String cui = "123456789"; //constante no se puede camabiar valor
        
        int numero = 10;  //variable si se puede cambiar el valor durante la ejecucion
        System.out.println("El valor del numero es: "+numero);
        System.out.println("Mi cui: "+cui);
        
        numero = 20;
        
      
        System.out.println("El valor del numero es: "+numero);
    
        
        
    }
    
}
